package com.rays.dto;

public class EmployeDTO {
	private int empId;
	private String empName;
	private String empEmail;
	private String empContect;
	private String empDestignation;
	private int salry;
	private String empPass;
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getEmpEmail() {
		return empEmail;
	}
	public void setEmpEmail(String empEmail) {
		this.empEmail = empEmail;
	}
	public String getEmpContect() {
		return empContect;
	}
	public void setEmpContect(String empContect) {
		this.empContect = empContect;
	}
	public String getEmpDestignation() {
		return empDestignation;
	}
	public void setEmpDestignation(String empDestignation) {
		this.empDestignation = empDestignation;
	}
	public int getSalry() {
		return salry;
	}
	public void setSalry(int salry) {
		this.salry = salry;
	}
	public String getEmpPass() {
		return empPass;
	}
	public void setEmpPass(String empPass) {
		this.empPass = empPass;
	}
	

}
